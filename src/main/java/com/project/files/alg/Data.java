package com.project.files.alg;

public class Data {
    private double e;
    private int id;

    public Data(){
        this.e = 0.0;
        this.id = 0;
    }

    public Data(double e, int id){
        this.e = e;
        this.id = id;
    }

    public double getE() {
        return e;
    }

    public int getId() {
        return id;
    }

}
